// Inline each template's files into src/templateAssets.generated.ts so the
// sandboxed extension can stage them WITHOUT reading its own install directory
// (Live's host permits fs reads only under storage/temp dirs — not the .ablx
// folder). Run by build-extension.mjs before every bundle; the checked-in
// output also lets tests/dev renders work without a build step.
import * as fs from 'node:fs';

const STYLES = {
  'pulse-waveform': ['index.html', 'timebridge.browser.js', 'template.json'],
  'project-feedback': ['index.html', 'template.json', 'gsap.min.js'],
};

const assets = {};
for (const [style, files] of Object.entries(STYLES)) {
  assets[style] = {};
  for (const f of files) {
    const p = `templates/${style}/${f}`;
    if (fs.existsSync(p)) assets[style][f] = fs.readFileSync(p, 'utf8');
  }
}

const studioHtml = fs.readFileSync('panel/index.html', 'utf8');

const out =
  '// AUTO-GENERATED by tools/gen-template-assets.mjs — do not edit by hand.\n' +
  '// Template + panel files inlined so the sandboxed extension can use them\n' +
  '// without reading its own install dir. Regenerated on every build:ext.\n' +
  'export const TEMPLATE_ASSETS: Record<string, Record<string, string>> =\n  ' +
  JSON.stringify(assets) +
  ';\n\n' +
  'export const STUDIO_HTML: string = ' +
  JSON.stringify(studioHtml) +
  ';\n';

fs.writeFileSync('src/templateAssets.generated.ts', out);
console.log('generated src/templateAssets.generated.ts');
